let has_env_debug =
  match Option.map String.lowercase_ascii (Sys.getenv_opt "AOC_DEBUG") with
  | None -> false
  | Some ("t" | "true" | "y" | "yes") -> true
  | Some _ -> false

let () =
  Logs.set_reporter
    (Logs.format_reporter
       ~dst:Format.std_formatter
       ~app:Format.std_formatter
       ());
  Logs.set_level
    (if has_env_debug
     then Some Logs.Debug
     else Some Logs.App)
